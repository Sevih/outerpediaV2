function getShareLinks(title: string, url: string): Record<Platform, string> {
  const t = encodeURIComponent(title);
  const u = encodeURIComponent(url);
  return {
    twitter: `https://x.com/intent/tweet?text=${t}&url=${u}`,
    facebook: `https://www.facebook.com/sharer/sharer.php?u=${u}`,
    reddit: `https://www.reddit.com/submit?url=${u}&title=${t}`,
    telegram: `https://t.me/share/url?url=${u}&text=${t}`,
    whatsapp: `https://wa.me/?text=${t}%20${u}`,
    line: `https://social-plugins.line.me/lineit/share?url=${u}`,
    kakaotalk: `https://story.kakao.com/share?url=${u}`,
    naver: `https://share.naver.com/web/shareView?url=${u}&title=${t}`,
    weibo: `https://service.weibo.com/share/share.php?url=${u}&title=${t}`,
    qq: `https://connect.qq.com/widget/shareqq/index.html?url=${u}&title=${t}`,
  };
}

export default function ShareButtons({ title, lang }: Props) {
  const [copied, setCopied] = useState(false);
  const [url, setUrl] = useState('');

  useEffect(() => {
    setUrl(window.location.href);
  }, []);

  const shareText = `${title} — Outerpedia`;
  const links = getShareLinks(shareText, url);
  const platforms = PLATFORMS_BY_LANG[lang];

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch { /* noop */ }
  };

  return (
    <div className="flex items-center gap-1.5">
      {platforms.map((p) => {
        const cfg = PLATFORM_CONFIG[p];
        return (
          <a
            key={p}
            href={links[p]}
            target="_blank"
            rel="nofollow noopener noreferrer"
            className={`flex items-center justify-center h-8 w-8 rounded-md bg-zinc-800 text-zinc-400 ${cfg.color} hover:bg-zinc-700 transition-colors`}
            aria-label={`Share on ${cfg.label}`}
            title={`Share on ${cfg.label}`}
          >
            <svg className="h-3.5 w-3.5 fill-current" viewBox="0 0 24 24" aria-hidden="true">
              <path d={cfg.icon} />
            </svg>
          </a>
        );
      })}

      <button
        onClick={handleCopy}
        className="relative flex items-center justify-center h-8 w-8 rounded-md bg-zinc-800 text-zinc-400 hover:text-zinc-100 hover:bg-zinc-700 transition-colors"
        aria-label="Copy link"
        title="Copy link"
        type="button"
      >
        {copied ? (
          <>
            <svg className="h-3.5 w-3.5 text-green-400" fill="none" viewBox="0 0 24 24" stroke="currentColor" aria-hidden="true">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
            </svg>
            <span className="absolute -top-7 left-1/2 -translate-x-1/2 rounded bg-green-600 px-2 py-0.5 text-xs text-white whitespace-nowrap pointer-events-none">
              Copied!
            </span>
          </>
        ) : (
          <svg className="h-3.5 w-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" aria-hidden="true">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 16H6a2 2 0 01-2-2V6a2 2 0 012-2h8a2 2 0 012 2v2m-6 12h8a2 2 0 002-2v-8a2 2 0 00-2-2h-8a2 2 0 00-2 2v8a2 2 0 002 2z" />
          </svg>
        )}
      </button>
    </div>
  );
}
